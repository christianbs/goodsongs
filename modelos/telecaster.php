                            <h1>Fender Telecaster</h1>
                            <hr style="color:black">
                            <p>Fender Telecaster, também conhecida como Tele, é uma típica guitarra elétrica de dois compartimentos, de corpo sólido construída pela Fender. O seu projeto simples, mas eficaz e seu som revolucionário inovou e criou tendências na fabricação de guitarra e na música popular. Inicialmente distribuída como Broadcaster no outono (primavera, no Brasil) de 1949, foi a primeira guitarra de seu tipo a ser produzida em uma escala significativa. Sua produção comercial pode ser traçada já em março de 1950, quando os modelos Squier simples e com dois captadores foram vendidos pela primeira vez.</p>
                            
                            
                            <h1>Origens</h1>
                            <hr style="color:black">
                            <p>
                          A Fender Telecaster foi desenvolvida por Leo Fender em Fullerton, Califórnia em 1949. No período de aproximadamente entre 1932-1949, muitos artesãos e empresas experimentaram guitarras elétricas de corpo sólido, mas nenhuma teve um impacto significativo no mercado. A Telecaster de Leo Fender foi o projeto que, finalmente, colocou a guitarra de corpo sólido no mapa.<br>

Na sua forma clássica, a guitarra é de construção extremamente simples, com o braço e escala produzidos com uma única peça de maple, aparafusado ao corpo feito de Alder, com a superfície plana na parte da frente e na parte de trás. O hardware inclui dois captadores single-coils selecionados através de uma chave de 3 posições(braço / braço + ponte / ponte) e dois knobs, sendo um de Tonalidade e outro de Volume.
