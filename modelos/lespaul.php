                            <h1>Lespaul</h1>
                            <hr style="color:black">
                            <p>Gibson Les Paul é uma guitarra de corpo sólido que começou a ser vendida em 1952. A Les                               Paul foi desenhada por Ted McCarty e criada em colaboração com o popular guitarrista Les                                    Paul, a quem a Gibson convidou para aprovar o novo modelo. Esse é um dos modelos de                                         guitarra mais conhecidos do mundo, juntamente com os modelos Stratocaster e Telecaster da                               Fender e SG da própria Gibson.</p>
                            
                            
                            <h1>Origens</h1>
                            <hr style="color:black">
                            <p>
                            A Gibson Les Paul foi o resultado de uma colaboração de design entre a Gibson Guitar                                        Corporation e o guitarrista de jazz e inventor Les Paul.Em 1950, com a introdução da Fender                                 Telecaster no mercado musical,as guitarras elétricas se tornaram uma febre entre o público.                                 Em reação a isso, o presidente da Gibson Ted McCarty trouxe o guitarrista Les Paul como                                 consultor.Les Paul era um respeitado inovador que tinha realizado experimentos com design de                                guitarras por anos para beneficiar a sua própria música.De fato, ele havia construído com                                  as próprias mãos um protótipo de corpo sólido chamado "The Log", um design mais tarde                                        considerado amplamente como o primeiro de corpo sólido já construído,(Spanish Guitar) em                                oposição da "Hawaiian", (uma guitarra de lap steel). <br/>                                                                        Essa guitarra é conhecida como "The Log" por causa do seu núcleo sólido que é um bloco                                     de pinho, cuja largura e profundidade são  um pouco maiores que a largura do                                               braço.Apesar de numerosos outros protótipos e modelos de corpo sólido de produção                                        limitada de outros fabricantes já tivessem vindo à tona, é sabido                                                          que entre 1945 e 1946 , Les Paul tinha se aproximado da Gibson com o protótipo da "The                                   Log" mas esse design de corpo sólido foi rejeitado.<br/>

                                Em 1951, essa rejeição inicial se tornou numa colaboração de design entre a Gibson Guitar                               Corporation e Les Paul. Foi acordado que a nova guitarra Les Paul seria um instrumento caro,                                e bem feito nos moldes da tradição da Gibson. Apesar de as lembranças serem diferentes                                   em relação a quem contribuiu com o que no design da Les Paul, ela estava longe de ser uma                               réplica de mercado dos modelos fabricados pela Fender.                         
                            </p>                
