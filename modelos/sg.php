                            <h1>Gibson SG</h1>
                            <hr style="color:black">
                            <p>Gibson SG é um modelo popular de guitarra elétrica de corpo sólido, surgido no começo dos                                    anos 60.</p>
                            
                            
                            <h1>Origens</h1>
                            <hr style="color:black">
                            <p>
                           Em meados de 1960 a Gibson Guitar Corporation sentiu que o modelo Les Paul, fabricado desde 1952, já esgotara seu apelo, e decidiu mudar o design. Várias mudanças mais sutis haviam ocorrido na Les Paul desde que havia sido introduzida em 1952 (a ponte ABR-1, os captadores Humbucker desenhados por Seth Lover, diferentes acabamentos e os trastes Jumbo em 1969) mas o que veio a seguir mudaria irremediavelmente o mundo das seis cordas. Duas críticas frequentemente dirigidas à Les Paul se referiam ao seu elevado peso e o difícil acesso aos últimos trastes, o que prejudicava a sua tocabilidade. E foi levando esses problemas em consideração e, provavelmente, influenciado pelos novos cenários musicais estabelecidos com o fim dos anos 50 que Ted McCarty e sua equipe de luthiers tomou a arriscada decisão de redesenhar a guitarra sólida da companhia na mais popular linha de guitarras Gibson até os dias de hoje.<br/>

Inicialmente a nova guitarra se chamava Les Paul SG. Por a modificação ter feito sem sua aprovação, Les Paul não gostou. Depois que o contrato de Les Paul com a Gibson terminou em 1962, a guitarra foi renomeada para "SG" (abreviação para "solid guitar", ou "guitarra sólida") no final de 1963.<br>
                                O corpo da SG é produzido em mogno. Bem mais fino que o da Les Paul tradicional, sem frisos, com dois chifres pontudos e com um acesso às últimas casas muito mais fácil em relação à sua antecessora. Quando introduzida, a SG vinha equipada com 2 ou 3 captadores humbuckers, um tremolo vibrola e um pequeno escudo de 5 camadas na parte inferior do corpo.

