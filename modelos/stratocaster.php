                            <h1>Origens</h1>
                            <hr style="color:black">
                            <p>
                          A Fender Stratocaster, também conhecida como "Strato" no Brasil, é um modelo de guitarra elétrica criada por Leo Fender, George Fullerton, e Freddie Tavares em 1954, produzida pela Fender Musical Instruments Corporation até os dias de hoje. O modelo apresenta double-cutaway (recorte duplo), com o cutaway superior maior que o inferior para balancear o peso da guitarra. A Stratocaster é utilizada por vários guitarristas que ao longo da história gravaram diversos clássicos com o modelo. Juntamente com a Gibson Les Paul e a Fender Telecaster, é um dos três modelos mais famosos de guitarra.<br/>

O design da Stratocaster tem sido copiado e modificado constantemente. É possível ver muitos modelos parecidos ou até mesmo com o mesmo design de outros fabricantes de guitarra.<br/>

Originalmente, a Stratocaster era feita na cor Sunburst de 2 cores, num corpo de Ash, braço em Maple (Carvalho americano)de peça única contendo 21 trastes, marcações estilo "bolinha" na escala e tarraxas Kluson até 1956, quando a Fender começou a utilizar Alder na produção dos corpos. Havia outras cores que não eram padrão e só eram feitas através de encomendas, até 1960. Esses modelos sob encomenda eram quase todos pintados com tinta automotiva produzida pela Dupont e encareciam a guitarra em 5%. O escudo de uma única camada, fixado com 8 parafusos permitiu que a parte eletrônica dos captadores ficasse protegida (expondo somente o jack/conector fêmea) e proporcionando uma proteção prática já que para ter acesso à fiação dos captadores só é preciso retirar o escudo. 

