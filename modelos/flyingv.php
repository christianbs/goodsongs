                            <h1>Gibson Flying V</h1>
                            <hr style="color:black">
                            <p>A Gibson Flying V, ou V Factor, é um modelo de guitarra fabricada pela Gibson em 1958.</p>
                            
                            
                            <h1>Origens</h1>
                            <hr style="color:black">
                            <p>
                           O primeiro protótipo desta guitarra foi feito em 1957. Ela, junto com a Futura, X-Plorer e a Moderne, iniciaram uma linha da guitarras modernistas desenhadas pelo então presidente da corporação, Ted McCarty. Estas guitarras vieram com o propósito de dar uma cara mais futurista à Gibson, mas elas inicialmente não vingaram. Depois do seu lançamento, em 1958, a linha foi interrompida em 1959.<br/>

No meio dos anos 60, guitarristas como Albert King, Lonnie Mack, Dave Davies, Cadu Hermeto e Jimi Hendrix, na procura de um visual mais arrojado e um som mais poderoso, começaram a usar Flying Vs. O interesse fez com que a Gibson pensasse em relançar o modelo.<br/>

E em 1967 ela de fato relançou o modelo, melhorando seu modelo com um escudo maior e mais bonito e trocando a ponte original, que tinha as cordas inseridas pela parte de trás do equipamento, pela ponte tradicional da Gibson. Alguns modelos eram fabricados com um trêmolo; este modelo é agora o padrão para a Flying V ou, como a Gibson agora a chama, V Factor.<br/>
                                Muitas outras marcas de instrumentos copiaram este modelo, muitas adicionando o seu "toque pessoal" para evitar qualquer complicação com a Gibson. Junto com os outros modelos que a Jackson fabricou a que se destaca é a personalizada para Randy Rhoads em 1981. Esta foi a primeira guitarra fabricada pela Jackson e ficou conhecida como "Concorde", por ter pontas assimétricas e pontiagudas, levemente curvadas, e corpo menor do que as fabricadas pela Gibson.Diz a lenda que Rhoads se inspirou nela, após viajar em uma aeronave Concorde, na turne de Blizzard of Ozz na Inglaaterra. Seguindo o modelo de Rhoads, a Jackson fabricou outra guitarra para o guitarrista do Ratt, Robbin Crosby e a fabricou com o nome de King V. Também foi fabricada uma guitarra para o guitarrista do Def Leppard, Phil Collen, denominada Jackson PC1, no formato "dinky". Outros modelos de guitarras em V são os modelos B.C. Rich Kerry King V, Jr. V, e Draco. Alguns outros fabricantes de guitarras em V são Dean, Cort, Antares e Ibanez.

