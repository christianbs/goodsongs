                        <h1>Introdução a introdução as Guitarras</h1>
                        <hr>
                        <p>O substantivo guitarra refere-se a uma série de instrumentos de cordas dedilhadas, que possuem geralmente de 6 a 12 cordas tensionadas ao longo do instrumento e possuem um corpo com formato aproximado de um 8 (embora também existam em diversos outros formatos), além de um braço, sobre o qual as cordas passam, permitindo ao executante controlar a altura da nota produzida.<br><br>
                            
                        Existem versões acústicas, que possuem caixa de ressonância e elétricas, que podem ou não possuir caixa de ressonância (ver: guitarra semiacústica), mas utilizam captadores e amplificadores para aumentar a intensidade sonora do instrumento.<br>

As guitarras, bem como a maior parte dos instrumentos de cordas são construídas por um luthier. O músico que a executa é chamado guitarrista.</p>
                   
                    <h1>Origens e Desenvolvimento</h1>
                    <hr>
                    <p>Instrumentos similares aos que hoje chamamos de guitarras existem ao menos há 5 mil anos. A guitarra parece derivar de outros instrumentos existentes anteriormente na Ásia Central.<br><br>
                        Instrumentos muito similares à guitarra aparecem em antigos alto-relevos e estátuas descobertas em Susa, na Pérsia (atualmente no Irã).<br><br>

A guitarra, em forma muito próxima à guitarra acústica atual, foi introduzida na Espanha no Século IX, mas não se conhece com precisão toda a história deste instrumento. No entanto há duas hipóteses mais prováveis para a introdução da guitarra no ocidente.<br><br>

A primeira hipótese é que a guitarra seria derivada da chamada kithára grega, que com o domínio do Império Romano passou a se chamar cítara romana, e era também denominada de fidícula. Teria chegado à Península Ibérica por volta do século I com os romanos.<br><br>
                        Esse instrumento se assemelhava à lira e posteriormente foram acontecendo as seguintes transformações: os seus braços dispostos da forma da lira foram se unindo, formando uma caixa acústica, à qual foi acrescentado um braço de três cravelhas e três cordas, e a esse braço foram feitas divisões transversais (trastes).<br><br>

A segunda hipótese é de que este instrumento seria derivado do antigo alaúde árabe, nome originado da palavra al ud, (a madeira), e que teria sido levado para a Península Ibérica através das invasões muçulmanas. O alaúde árabe que penetrou na península nessa época foi um instrumento que se adaptou perfeitamente às atividades culturais e, em pouco tempo, fazia parte das atividades da corte.<br><br>

Outra hipótese é de que foram aplicadas as técnicas do alaúde (cordas beliscadas, número de cordas, afinação, etc.) a instrumentos de corda friccionada (nessa altura chamadas “violas”). Isso explicaria o fato de em espanhol ter havido a distinção entre vihuela de arco (viola tocada com um arco) e vihuela de mano (viola tocada com a mão). </p>
                    
                            
                    <h1>Desenvolvimentos posteriores</h1>
                    <hr>
                    <p>A vihuela espanhola parece ser um instrumento intermediário entre o alaúde e a guitarra moderna, pois possui uma afinação semelhante ao alaúde, mas o corpo já tinha o formato em 8 semelhante, sendo menor, que as guitarras atuais. No entanto não é certo se esta é mesmo uma forma de transição ou apenas um instrumento que combina características dos dois instrumentos. <br><br>
                                Em favor da segunda hipótese, argumenta-se que a remodelagem da vihuela para se tornar parecida com a guitarra foi uma forma de diferenciar visualmente o instrumento ocidental do alaúde árabe associado aos invasores[carece de fontes]. Esta variedade sofreu alterações em Portugal e deu origem às violas modernas.<br><br>

Durante vários séculos de história a guitarra acústica ganhou diversas variedades. Há grandes variações em todas as características dos instrumentos: o tamanho e o formato da caixa de ressonância, o formato e a quantidade de aberturas frontais, o comprimento do braço, a quantidade das cordas, a extensão e a forma de afinação. Certas variedades se desenvolveram separadamente e se tornaram instrumentos específicos.<br><br>

Além disso há algumas variedades que são frequentemente associadas ao género musical em que são usadas, como as guitarras de blues, folk, jazz e a guitarra clássica. Embora sejam fundamentalmente o mesmo instrumento, a variedade utilizada no flamenco, por exemplo, é diferente daquela utilizada na música clássica.<br><br>

Segundo Paco de Lucía, o inventor da guitarra tal como a conhecemos se chama Zyryab. Nascido em Bagdá, ele viveu no fim do século VIII na corte de Córdoba. Ele introduziu uma quinta corda ao 'ud árabe e fundou uma escola de música que exerceu influência considerável sobre a música árabe-andaluz.<br><br>

Foi Antonio de Torres, um luthier espanhol do século XIX que deu à guitarra a forma e as dimensões da guitarra clássica atual, a partir do qual, diversas outras variedades surgiram no século XX (como a guitarra de jazz, a guitarra folk e a elétrica).<br><br>

A guitarra elétrica surgiu, independentemente, pela mão de diversas pessoas nos anos 30. Inicialmente a eletrificação consistia em usar o próprio instrumento acústico com um microfone de voz dentro de sua caixa de ressonância. Mais tarde esse microfone foi substituído pelo microfone de contato chamado captador ou, em inglês pickup.<br><br>

Por nem sempre ser necessária uma caixa de ressonância acústica numa guitarra eléctrica, surgiram as primeiras guitarras maciças (Fender Stratocaster e Gibson Les Paul) nas décadas de 1950 e 60. As cordas passaram a ser metálicas e captadores magnéticos de indução começaram a ser utilizados.</p>
